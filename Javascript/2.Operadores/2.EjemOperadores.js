//* EJEMPLO DE USO PARA LAS OPERACIONES MATEMÁTICAS.

// Declaración de variables
let x = 5;
let y = 3;

// Suma
let suma = x + y;
console.log("Suma: " + suma);  // 5 + 3 = 8

// Resta
let resta = x - y;
console.log("Resta: " + resta);  // 5 - 3 = 2

// Multiplicación
let multiplicacion = x * y;
console.log("Multiplicación: " + multiplicacion);  // 5 * 3 = 15

// División
let division = x / y;
console.log("División: " + division);  // 5 / 3 = 1.666...

// Módulo
let modulo = x % y;
console.log("Módulo: " + modulo);  // 5 % 3 = 2

// Potencia
let potencia = x ** y;
console.log("Potencia: " + potencia);  // 5 ** 3 = 125

// Incremento
x++;  // o ++x
console.log("Incremento: " + x);  // 5 incrementado = 6

// Decremento
y--;  // o --y
console.log("Decremento: " + y);  // 3 decrementado = 2
